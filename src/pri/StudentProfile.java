    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try
        {
            String name = jTextFieldName.getText();
            String dob = jTextFieldDOB.getText();
            String address = jTextAreaAddress.getText();
            int phone = Integer.parseInt(jTextFieldPhone.getText());
            String email = jTextFieldEmail.getText();
            Connect con = new Connect();
            if(con.updateStudent(name, dob,address,phone,email,Sid))
            {
                JOptionPane.showMessageDialog(this,"updation completed");
                this.dispose();
            }
            else
            {
                JOptionPane.showMessageDialog(this,"Failed to Update");
            }
        }
        catch(NumberFormatException | HeadlessException e)
        {
            e.printStackTrace();
            System.out.println("Error in AdminRegister.java");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

