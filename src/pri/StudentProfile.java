    private void formInternalFrameOpened(javax.swing.event.InternalFrameEvent evt) {//GEN-FIRST:event_formInternalFrameOpened
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:
            Connect con = new Connect();
            ResultSet rs = con.getStudent(Sid);
            while(rs.next())
            {
                jTextFieldName.setText(rs.getString("Name"));
                jTextFieldDOB.setText(rs.getString("DOB"));
                jTextAreaAddress.setText(rs.getString("Address"));
                jTextFieldPhone.setText(Integer.toString(rs.getInt("Phone")));
                jTextFieldEmail.setText(rs.getString("Email"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(AdminProfile.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_formInternalFrameOpened


